/**
 * Returns the full BrandVoice configuration for a given personality mode.
 */
export function getPersonality(mode: PersonalityMode): BrandVoice {
  return PERSONALITIES[mode] ?? PERSONALITIES.luxury;
}

/**
 * Returns the greeting message for a given personality mode.
 */
export function getGreeting(mode: PersonalityMode): string {
  return getPersonality(mode).greeting;
}

/**
 * Returns all 5 personalities for selection UI.
 */
export function getAllPersonalities(): BrandVoice[] {
  return Object.values(PERSONALITIES);
}

/**
 * Builds the full system prompt with personality + dish context + cart context.
 */
export function getSystemPrompt(
  mode: PersonalityMode,
  dishContext?: {
    name?: string;
    description?: string;
    price?: number;
    addons?: Array<{ name: string; price: number }>;
    pairings?: string[];
  },
  cartItems?: Array<{ name: string; totalPrice: number }>
): string {
  const personality = getPersonality(mode);
  let prompt = personality.systemPrompt;

  // Add prohibited words reminder
  if (personality.prohibitedWords.length > 0) {
    prompt += `\n\n⚠️ كلمات ممنوع استخدامها: ${personality.prohibitedWords.join('، ')}`;
  }

  // Add catchphrases reminder
  if (personality.catchphrases.length > 0) {
    prompt += `\n\n💬 عباراتك المميزة (استخدمها بشكل طبيعي): ${personality.catchphrases.join(' | ')}`;
  }

  // Add dish context
  if (dishContext) {
    prompt += `\n\n📋 الصنف الحالي الذي ينظر إليه العميل:`;
    if (dishContext.name) prompt += `\n- الاسم: ${dishContext.name}`;
    if (dishContext.description) prompt += `\n- الوصف: ${dishContext.description}`;
    if (dishContext.price) prompt += `\n- السعر: ${dishContext.price} ريال`;
    if (dishContext.addons && dishContext.addons.length > 0) {
      prompt += `\n- الإضافات المتاحة: ${dishContext.addons.map((a) => a.name).join('، ')}`;
    }
    if (dishContext.pairings && dishContext.pairings.length > 0) {
      prompt += `\n- يتناسب مع: ${dishContext.pairings.join('، ')}`;
    }
  }

  // Add cart context
  if (cartItems && cartItems.length > 0) {
    const total = cartItems.reduce((sum, item) => sum + (item.totalPrice || 0), 0);
    const itemNames = cartItems.map((item) => item.name).join('، ');
    prompt += `\n\n🛒 سلة العميل الحالية:`;
    prompt += `\n- الأصناف: ${itemNames}`;
    prompt += `\n- الإجمالي: ${total} ريال`;
    prompt += `\nاقترح إضافات مناسبة لزيادة قيمة الطلب حسب أسلوبك في التوصية.`;
  }

  return prompt;
}